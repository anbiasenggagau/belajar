const mongoose=require('mongoose')


const Subscribers=new mongoose.model('Subscriber', {
    name: {
        type: String,
        required: true
    },
    subscribedToChannel: {
        type: String,
        required: true
    },
    subscribeDate: {
        type: Date,
        required: true,
        default: Date.now
    }
})

module.exports=Subscribers